function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}
function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

console.log("Add:", add(1, 2));
console.log("Subtract:", subtract(1, 2));
console.log("Multiply:", multiply(1, 2));
console.log("Divide:", divide(1, 2));
